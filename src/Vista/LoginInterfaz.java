        usuario = txtUsuario.getText();
        contrasena = String.valueOf(txtContrasena.getPassword());
        Login l = new Login();
        boolean validado = l.validarLogin(usuario, contrasena);
        if(validado){
            SistemaInterfaz sis = new SistemaInterfaz();
            sis.setVisible(validado);
            dispose();
        }
